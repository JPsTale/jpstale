package org.jpstale.entity;

/**
 * 角色属性
 * @author yanmaoyuan
 *
 */
public class State {
	int strength; // 力量
	int spirit; // 精神
	int talent; // 才能
	int dexterity; // 敏捷
	int health; // 体质
	public int getStrength() {
		return strength;
	}
	public int getSpirit() {
		return spirit;
	}
	public int getTalent() {
		return talent;
	}
	public int getDexterity() {
		return dexterity;
	}
	public int getHealth() {
		return health;
	}
	
}
